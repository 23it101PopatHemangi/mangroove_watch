// @desc    Get user leaderboard
// @route   GET /api/auth/leaderboard
// @access  Public
export const getLeaderboard = async (req, res) => {
  try {
    const { page = 1, limit = 20 } = req.query;

    const users = await User.find({})
      .select('username firstName lastName points level reportsCount avatar')
      .sort({ points: -1, reportsCount: -1 })
      .limit(parseInt(limit))
      .skip((parseInt(page) - 1) * parseInt(limit));

    const total = await User.countDocuments({});

    res.json({
      success: true,
      data: users,
      pagination: {
        page: parseInt(page),
        limit: parseInt(limit),
        total,
        pages: Math.ceil(total / parseInt(limit))
      }
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: 'Server error',
      error: error.message
    });
  }
};

// @desc    Get user stats
// @route   GET /api/auth/stats
// @access  Private
export const getUserStats = async (req, res) => {
  try {
    const user = await User.findById(req.user._id);
    if (!user) {
      return res.status(404).json({
        success: false,
        message: 'User not found'
      });
    }

    // Calculate additional stats
    const stats = {
      totalPoints: user.points,
      currentLevel: user.level,
      reportsSubmitted: user.reportsCount,
      rank: await User.countDocuments({ points: { $gt: user.points } }) + 1,
      nextLevelPoints: user.level === 'Master' ? null : 
        user.level === 'Expert' ? 1000 - user.points :
        user.level === 'Contributor' ? 500 - user.points :
        100 - user.points
    };

    res.json({
      success: true,
      data: stats
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: 'Server error',
      error: error.message
    });
  }
};
